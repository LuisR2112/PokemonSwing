    private void cargaMascotas() {
        DefaultListModel<String> modelMascotasView = new DefaultListModel<>();
        if(this.MascotasView!=null){
            
            if(this.MascotasView.length>0){
                for(Mascota m : MascotasView){
                    modelMascotasView.addElement(m.getNombre());
                }
            }
           else{
                modelMascotasView.addElement("Sin Mascotas");
            }   
            jList1.setModel(modelMascotasView);
        }else{
            JOptionPane.showMessageDialog(null, "Error en el sistema al cargar las mascotas");
        }
        
    }
