    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        try {
            if(!this.jList1.getSelectedValue().equalsIgnoreCase("sin mascotas")){
                jLabel4.setIcon(new ImageIcon(EntrenadorControlador.dameEntrenadorActual().getMascotas()[jList1.getSelectedIndex()].getUrlFoto()));
                this.mascotaSelected = EntrenadorControlador.dameEntrenadorActual().getMascotas()[jList1.getSelectedIndex()];
                jLabel7.setText("Nombre: "+this.mascotaSelected.getNombre());
                jLabel8.setText("Nivel: "+this.mascotaSelected.getNivel());
                jLabel9.setText("Peleas:" +this.mascotaSelected.getSigNivel());
                jLabel10.setText(this.mascotaSelected.getEtapa().toString());
            }else{
                JOptionPane.showMessageDialog(null, "Aun no tienes mascotas");
            }
        } catch (Exception ex) {
            Logger.getLogger(frmPerfil.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jList1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(mascotaSelected!=null){
            if(mascotaSelected.getEtapa().toString().equalsIgnoreCase("MUERTO")){
                if(JOptionPane.showInputDialog(null,"Precio de para revivir: "+mascotaSelected.getPrecioRevivir()).equalsIgnoreCase("Si")){
                    try {
                        if(EntrenadorControlador.dameEntrenadorActual().getDinero()>=mascotaSelected.getPrecioRevivir()){
                            EntrenadorControlador.dameEntrenadorActual().setDinero(EntrenadorControlador.dameEntrenadorActual().getDinero()-mascotaSelected.getPrecioRevivir());
                            MascotasView[jList1.getSelectedIndex()].setEtapa(EtapaMascota.NACIMIENTO);
                            jLabel10.setText(mascotaSelected.getEtapa().toString());
                            jLabel6.setText("Balance: "+EntrenadorControlador.dameEntrenadorActual().getDinero());
                            JOptionPane.showMessageDialog(null, "Mascota Revivida con exito");
                        }else{
                            JOptionPane.showMessageDialog(null, "No tiene dinero suficiente");
                        }
                    } catch (Exception e) {
                    }
                }
            }else{
                JOptionPane.showMessageDialog(null, "Esta mascota aun esta viva");
            }
        }else{
            JOptionPane.showMessageDialog(null, "No tiene Pokemon seleccionado");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

